sayi1=int(input("sayi1 giriniz "))
sayi2=int(input("sayi2 giriniz "))
islem=input("islem seciniz (toplama:+, cikarma:-, carpma:*, bolme:/)")
if islem=="+":
    print(sayi1 + sayi2)
elif islem=="-":
    print(sayi1 - sayi2)
elif islem=="*":
    print(sayi1 * sayi2)
elif islem=="/":
    print(sayi1 / sayi2)
